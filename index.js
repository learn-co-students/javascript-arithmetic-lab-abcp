function add(a,b){
  return a + b
}

function subtract(c,d){
  return c - d
}

function multiply(e,f){
  return e * f
}

function divide(g,h){
  return g/h
}

function inc(n){
  return n += 1
}

function dec(n){
  return n -= 1
}

function makeInt(A){
  return parseInt(`${A}`, 10)
}

function preserveDecimal(B){
  return parseFloat(`${B}`)
}