//ONE
function add(a,b)
{
  return a+b
}

//TWO
function subtract(a,b)
{
  return a-b
}

//THREE
function multiply(a,b)
{
  return a*b
}

//FOUR
function divide(a,b)
{
  return a/b
}

//FIVE
function inc(n)
{
  n++;
  return n
}

//SIX
function dec(n)
{
  n--;
  return n
}

//SEVEN
function makeInt(n)
{
  return parseInt(n, 10);
}

//EIGHT
function preserveDecimal(n)
{
  return parseFloat(n)
}





