function add(one, two){
  return one + two
}

function subtract(one, two){
  return one - two
}

function multiply(one, two){
  return one * two
}

function divide(one, two){
  return one / two
}

function inc(n){
  return n += 1
}

function dec(n){
  return n -= 1
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}